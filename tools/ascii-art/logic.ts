export function generateAsciiArt(text: string): string {
  const chars = text.toUpperCase().split('');
  const height = 5;
  const lines: string[] = Array(height).fill('');
  for (const c of chars) {
    if (c === ' ') {
      for (let i = 0; i < height; i++) lines[i] += '   ';
      continue;
    }
    const block = BLOCKS[c];
    if (block) {
      for (let i = 0; i < height; i++) lines[i] += (block[i] || '') + ' ';
    } else {
      for (let i = 0; i < height; i++) lines[i] += c.repeat(4) + ' ';
    }
  }
  return lines.join('\n');
}
