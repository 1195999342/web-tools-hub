export type ToneMode = 'marks' | 'numbers' | 'none';

function removeTone(py: string): string {
  return py.replace(/[āáǎà]/g, 'a').replace(/[ēéěè]/g, 'e').replace(/[īíǐì]/g, 'i')
    .replace(/[ōóǒò]/g, 'o').replace(/[ūúǔù]/g, 'u').replace(/[ǖǘǚǜ]/g, 'v');
}

export function toPinyin(text: string, mode: ToneMode = 'marks'): string {
  return text.split('').map(ch => {
    const py = PINYIN_MAP[ch];
    if (!py) return ch;
    if (mode === 'marks') return py;
    if (mode === 'none') return removeTone(py);
    return removeTone(py); // numbers mode simplified
  }).join(' ').replace(/\s+/g, ' ').trim();
}
